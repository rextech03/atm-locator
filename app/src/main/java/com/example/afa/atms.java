package com.example.afa;

public class atms {


        private String lon;
        private String lat;
        private String address;

public void Atms(String lon, String lat, String address){
            this.lon = lon;
            this.lat = lat;
            this.address = address;

        }

        public String getLon(){
            return lon;
        }
        public String getLat(){
            return lon;
        }
        public String getAddress(){
            return address;
        }

    }


